// Smart search function to find relevant responses
export const findBestResponse = (query, userName = '') => {
  const queryLower = query.toLowerCase();
  const greeting = userName ? `${userName}, ` : '';
  
  // Search through all categories and topics
  for (const [category, topics] of Object.entries(knowledgeBase)) {
    for (const [topicKey, topic] of Object.entries(topics)) {
      // Check if any keywords match
      const keywordMatch = topic.keywords.some(keyword => 
        queryLower.includes(keyword.toLowerCase())
      );
      
      if (keywordMatch) {
        return `${greeting}${topic.response}`;
      }
    }
  }
  
  return null; // No match found
};

export default knowledgeBase;


